import os as _os  # noqa: E402

_gendir = _os.path.join(_os.path.dirname(__file__), "gen")
if _os.path.isdir(_gendir):
    _seen = {(t.get("name") or "").lower().strip() for t in LOCATION_EXAMPLES}
    for _fn in sorted(_os.listdir(_gendir)):
        if not _fn.endswith(".json"):
            continue
        try:
            with open(_os.path.join(_gendir, _fn), encoding="utf-8") as _f:
                _data = _json.load(_f)
        except Exception:
            continue
        for _t in (_data if isinstance(_data, list) else []):
            _k = (_t.get("name") or "").lower().strip()
            if not _k or _k in _seen:
                continue
            _seen.add(_k)
            LOCATION_EXAMPLES.append(_t)
