        if not _fn.endswith(".json"):
            continue
        with open(_os.path.join(_gendir, _fn), encoding="utf-8") as _f:
            for _e in _json.load(_f):
                _k = (_e.get("mode"), _e.get("gold"))
                if _k not in _seen and _e.get("gold"):
                    _seen.add(_k)
